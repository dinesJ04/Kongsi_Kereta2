package mobile.wsmb2024.C10;

public class CarModel {
    public String icnum;
    public String model;
    public String capacity;
    public String number;

    public CarModel(){

    }

    public CarModel(String icnum, String model, String capacity, String number) {
        this.icnum = icnum;
        this.model = model;
        this.capacity = capacity;
        this.number = number;
    }

}
